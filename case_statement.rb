weather = "rainy"
case weather
	when "sunny"
		puts "Hooray for the sunshine. Remember to wear a hat and sunblock!"
	when "cloudy"
		puts "You may want something a bit warmer, like a sweater."
	when "rainy"
		puts "Don the rain jacket and rain boots to stay dry!"
	when "foggy"
		puts "You may need to wear something to keep you warm and dry, like a good coat."
	when "humid"
		puts "Wear something lightweight to keep you cool."
	when "snowy"
		puts "Don't forget the boots and parka!"
	else
		puts "Hmmm, I'm not sure which weather conditions you are referring to, sorry!"
end